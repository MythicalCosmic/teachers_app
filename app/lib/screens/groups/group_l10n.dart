  static String text(
    BuildContext context,
    String key, {
    Map<String, Object> values = const {},
  }) {
    final code = Localizations.maybeLocaleOf(context)?.languageCode ?? 'uz';
    var result = _copy[code]?[key] ?? _copy['uz']?[key] ?? key;
    for (final entry in values.entries) {
      result = result.replaceAll('{${entry.key}}', '${entry.value}');
    }
    return result;
  }

  static String month(BuildContext context, int month) {
    final code = Localizations.maybeLocaleOf(context)?.languageCode ?? 'uz';
    return (_months[code] ?? _months['uz']!)[month - 1];
  }

  static String weekday(BuildContext context, int weekday) {
    final code = Localizations.maybeLocaleOf(context)?.languageCode ?? 'uz';
    return (_weekdays[code] ?? _weekdays['uz']!)[weekday - 1];
